        public int ЗаписатьСтроку(string знСтрока)
        {
            if (!ЗначениеЗаполнено(знСтрока)) return 0;
            
            Массив м2;
            var мСтр = Стр.Разделить(знСтрока, " ");
            if (мСтр.Length == 1)
                м2 = МассивИзСтроки(мСтр[0]);
            else
            {
                м2 = Новый.Массив();
                м2.Добавить(2); // предложение
                foreach (var эСтр in мСтр)
                    м2.Добавить(ДобавитьЗначение(МассивИзСтроки(эСтр)));
            }

            return ДобавитьЗначение(м2);
        }


        public string ПолучитьСтроку(int нз)
        {
            if (нз == 0) return "";
            
            var м2 = Новый.Массив();
            var мСтр = ПолучитьМассив(нз);
            if ((int)мСтр[0] == 1) // слово
                м2.Добавить(мСтр);
            else // предложение
            {
                var н1 = мСтр.Количество();
                for (var н = 1; н < н1; н++)
                    м2.Добавить(ПолучитьМассив((int)мСтр[н]));
            }

            var зСтр = "";
            var пр = "";
            foreach (Массив м1 in м2)
            {
                зСтр = зСтр + пр;
                var н1 = м1.Количество();
                for (var н = 1; н < н1; н++)
                    зСтр += Символ((int)м1[н]);
                пр = " ";
            }

            return зСтр;
        } // ПолучитьСтроку()


