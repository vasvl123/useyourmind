        // открыть контейнер для чтения или записи
        public bool ОткрытьПотокДанных(bool ДляЗаписи = false, object Позиция = null)
        {

            try
            {

                if (ДляЗаписи)
                {

                    if (!(ПотокДанных == Неопределено))
                    {
                        if (!(ПотокДанных.ДоступнаЗапись))
                        {
                            ПотокДанных.Закрыть();
                            ПотокДанных = null;
                        }
                    }

                    if (ПотокДанных == Неопределено)
                    {
                        ПотокДанных = МенеджерФайловыхПотоков.ОткрытьДляЗаписи(ИмяФайлаДанных);
                    }

                    if (Позиция == Неопределено)
                    {
                        ПотокДанных.Перейти(0, ПозицияВПотоке.Конец);
                    }
                    else
                    {
                        ПотокДанных.Перейти((int)Позиция, ПозицияВПотоке.Начало);
                    }

                    //ВремяИзменения = ТекущаяДата();

                }
                else
                {

                    if (!(ПотокДанных == Неопределено))
                    {
                        if (!(ПотокДанных.ДоступноЧтение))
                        {
                            ПотокДанных.Закрыть();
                            ПотокДанных = null;
                        }
                    }

                    if (ПотокДанных == Неопределено)
                    {
                        ПотокДанных = МенеджерФайловыхПотоков.ОткрытьДляЧтения(ИмяФайлаДанных);
                    }

                    if (!(Позиция == Неопределено))
                    {
                        ПотокДанных.Перейти((int)Позиция, ПозицияВПотоке.Начало);
                    }

                }

                return Истина;

            }
            catch (Exception e)
            {

                Сообщить(ОписаниеОшибки(e));
                return Ложь;

            }

        }


        public Узел ЗаписатьСлово(string Слово, int пКод = 0)
        {
            var нУзел = ПрочитатьУзел();
            
            for (var н = 1; н <= Стр.Длина(Слово); н++)
            {
                var нКод = (int)Число(нУзел.Код);
                var с = Сред(Слово, н, 1);
                if (нУзел._Дочерний is int) 
                {
                    if ((int)нУзел._Дочерний == 0)
                    {
                        нУзел.Дочерний = Узел.Новый("Имя, Значение", с, нКод);
                        ЗаписатьУзел(нУзел.Дочерний);
                    }
                    else 
                        нУзел.Дочерний = ПрочитатьУзел((int)нУзел._Дочерний);
                }
                var дУзел = нУзел.Дочерний;

                while (дУзел.Имя != с)
                {
                    if (дУзел._Соседний is int)
                    {
                        if ((int)дУзел._Соседний == 0) break;
                    }
                    else 
                        дУзел.Соседний = ПрочитатьУзел((int)дУзел._Соседний);

                    дУзел = дУзел.Соседний;
                }
                
                if  (дУзел.Имя != с)
                {
                    дУзел.Соседний = Узел.Новый("Имя, Значение", с, нКод);
                    ЗаписатьУзел(дУзел.Соседний);
                    дУзел = дУзел.Соседний;
                }

                нУзел = дУзел;
            }

            // создать ссылку на предыдущее слово

            if (нУзел._Атрибут is int) 
            {
                if ((int)нУзел._Атрибут == 0)
                {
                    нУзел.Атрибут = Узел.Новый("Значение", пКод);
                    ЗаписатьУзел(нУзел.Атрибут);
                }
                else 
                    нУзел.Атрибут = ПрочитатьУзел((int)нУзел._Атрибут);
            }
            var аУзел = нУзел.Атрибут;

            while ((int)аУзел.Значение != пКод)
            {
                if (аУзел._Соседний is int)
                {
                    if ((int)аУзел._Соседний == 0) break;
                }
                else 
                    аУзел.Соседний = ПрочитатьУзел((int)аУзел._Соседний);

                аУзел = аУзел.Соседний;
            }
            
            if ((int)аУзел.Значение != пКод)
            {
                аУзел.Соседний = Узел.Новый("Значение", пКод);
                ЗаписатьУзел(аУзел.Соседний);
                аУзел = аУзел.Соседний;
            }

            return аУзел;
        }

        public int ЗаписатьУзел(Узел Узел)
        {
            var пИмя = ЗаписатьСтроку(Узел.Имя);
            var пЗначение = ЗаписатьСтроку(Узел.Значение as string);

            int Код = (int)ПотокДанных.Размер();
                        
            if (Лев(Узел.Код, 1) != "n") // не новый
                Код = (int)Число(Узел.Код);

            Узел.Код = Код.ToString();

            var буф = БуферДвоичныхДанных.Новый(20);

            if (Узел._Дочерний is int)
                буф.ЗаписатьЦелое32(0, (int)Узел._Дочерний);
            else if (Узел.Дочерний != null)
                буф.ЗаписатьЦелое32(0, (int)Число(Узел.Дочерний.Код));

            if (Узел._Соседний is int)
                буф.ЗаписатьЦелое32(4, (int)Узел._Соседний);
            else if (Узел._Соседний != null)
                буф.ЗаписатьЦелое32(4, (int)Число(Узел.Соседний.Код));

            if (Узел._Атрибут is int)
                буф.ЗаписатьЦелое32(8, (int)Узел._Атрибут);
            else if (Узел._Атрибут != null)
                буф.ЗаписатьЦелое32(8, (int)Число(Узел.Атрибут.Код));

            буф.ЗаписатьЦелое32(12, пИмя);
            буф.ЗаписатьЦелое32(16, пЗначение);

            ОткрытьПотокДанных(Истина);
            var н = Код;
            if (н == 1) н = 20;
            ПотокДанных.Перейти(н, ПозицияВПотоке.Конец);
            ПотокДанных.Записать(буф, 0, 20);
            ПотокДанных.СброситьБуферы();

            return Код;
        }


